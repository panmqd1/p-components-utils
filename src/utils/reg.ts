/** 6位数字验证码正则 */
export const REGEXP_SIX = /^\d{6}$/;

/** 密码正则（密码格式应为8-20位数字、字母、符号的任意两种组合） */
// NOTE 密码必须包括：大写字母，小写字母，数字，且长度为8至20位
export const REGEXP_PWD =
  /^(?![0-9]+$)(?![a-z]+$)(?![A-Z]+$)(?!([^(0-9a-zA-Z)]|[()])+$)(?!^.*[\u4E00-\u9FA5].*$)([^(0-9a-zA-Z)]|[()]|[a-z]|[A-Z]|[0-9]){8,20}$/;

/** 手机号码验证 */
export const REGEXP_PHONE = /^(?:(?:\+|00)86)?1\d{10}$/;

// 联系方式验证, 11位或12位数字
export const EXTRA_REGEXP_PHONE = /^\d{11,12}$/;
